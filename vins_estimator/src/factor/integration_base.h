class IntegrationBase
{
  public:
    IntegrationBase() = delete;
    IntegrationBase(const Eigen::Vector3d &_acc_0, const Eigen::Vector3d &_gyr_0,
                    const Eigen::Vector3d &_linearized_ba, const Eigen::Vector3d &_linearized_bg)
        : acc_0{_acc_0}, gyr_0{_gyr_0}, linearized_acc{_acc_0}, linearized_gyr{_gyr_0},
          linearized_ba{_linearized_ba}, linearized_bg{_linearized_bg},
            jacobian{Eigen::Matrix<double, 15, 15>::Identity()}, covariance{Eigen::Matrix<double, 15, 15>::Zero()},
          sum_dt{0.0}, delta_p{Eigen::Vector3d::Zero()}, delta_q{Eigen::Quaterniond::Identity()}, delta_v{Eigen::Vector3d::Zero()}

    {
        noise = Eigen::Matrix<double, 18, 18>::Zero();
        noise.block<3, 3>(0, 0) =  (ACC_N * ACC_N) * Eigen::Matrix3d::Identity();
        noise.block<3, 3>(3, 3) =  (GYR_N * GYR_N) * Eigen::Matrix3d::Identity();
        noise.block<3, 3>(6, 6) =  (ACC_N * ACC_N) * Eigen::Matrix3d::Identity();
        noise.block<3, 3>(9, 9) =  (GYR_N * GYR_N) * Eigen::Matrix3d::Identity();
        noise.block<3, 3>(12, 12) =  (ACC_W * ACC_W) * Eigen::Matrix3d::Identity();
        noise.block<3, 3>(15, 15) =  (GYR_W * GYR_W) * Eigen::Matrix3d::Identity();
    }

    void push_back(double dt, const Eigen::Vector3d &acc, const Eigen::Vector3d &gyr)
    {
        dt_buf.push_back(dt);
        acc_buf.push_back(acc);
        gyr_buf.push_back(gyr);
        propagate(dt, acc, gyr);
    }

    //优化过程中Bias会更新，需要根据新的bias重新计算预积分
    void repropagate(const Eigen::Vector3d &_linearized_ba, const Eigen::Vector3d &_linearized_bg)
    {
        sum_dt = 0.0;
        acc_0 = linearized_acc;
        gyr_0 = linearized_gyr;
        delta_p.setZero();
        delta_q.setIdentity();
        delta_v.setZero();
        linearized_ba = _linearized_ba;
        linearized_bg = _linearized_bg;
        jacobian.setIdentity();
        covariance.setZero();
        for (int i = 0; i < static_cast<int>(dt_buf.size()); i++)
            propagate(dt_buf[i], acc_buf[i], gyr_buf[i]);
    }

    /**
    * @brief   IMU预积分中采用中值积分递推Jacobian和Covariance
    *          构造误差的线性化递推方程，得到Jacobian和Covariance递推公式-> Paper 式9、10、11
    * @return  void
    */
    void midPointIntegration(double _dt, 
                            const Eigen::Vector3d &_acc_0, const Eigen::Vector3d &_gyr_0,
                            const Eigen::Vector3d &_acc_1, const Eigen::Vector3d &_gyr_1,
                            const Eigen::Vector3d &delta_p, const Eigen::Quaterniond &delta_q, const Eigen::Vector3d &delta_v,
                            const Eigen::Vector3d &linearized_ba, const Eigen::Vector3d &linearized_bg,
                            Eigen::Vector3d &result_delta_p, Eigen::Quaterniond &result_delta_q, Eigen::Vector3d &result_delta_v,
                            Eigen::Vector3d &result_linearized_ba, Eigen::Vector3d &result_linearized_bg, bool update_jacobian)
    {
        //ROS_INFO("midpoint integration");
        //(cxn)中值预积分
        // q_{bibk}(a^{bk}-b^a_k)
        Vector3d un_acc_0 = delta_q * (_acc_0 - linearized_ba);
        // w = 1/2 (w^{bk} + w^{bk+1}) - b^g_k
        Vector3d un_gyr = 0.5 * (_gyr_0 + _gyr_1) - linearized_bg;
        // q_{bibk+1} = q_{bibk} [1 ; 1/2*w*dt]
        result_delta_q = delta_q * Quaterniond(1, un_gyr(0) * _dt / 2, un_gyr(1) * _dt / 2, un_gyr(2) * _dt / 2);
        // q_{bibk+1}(a^{bk+1}-b^a_k)
        Vector3d un_acc_1 = result_delta_q * (_acc_1 - linearized_ba);
        // a=1/2 (q_{bibk+1}(a^{bk+1}-b^a_k)+q_{bibk}(a^{bk}-b^a_k))
        Vector3d un_acc = 0.5 * (un_acc_0 + un_acc_1);
        // alpha_{bibk+1} = alpha_{bibk} + beta_{bibk} dt + 1/2 a dtdt
        result_delta_p = delta_p + delta_v * _dt + 0.5 * un_acc * _dt * _dt;
        // beta_{bibk+1} = beta_{bibk} + a dt
        result_delta_v = delta_v + un_acc * _dt;
        // b^a_{k+1} = b^a_{k}
        result_linearized_ba = linearized_ba;
        // b^g_{k+1} = b^g_{k}
        result_linearized_bg = linearized_bg;         

        /*(cxn) IMU在每个时刻积分出来的值都是有误差的，我们需要对误差进行分析
        我们中值预积分的时候是通过 x_k = [alpha_{bk},theta_{bk},beta_{bk},b^a_{bk},b^g_{bk}] 推导出 x_{k+1}
        有：x_{k+1} = f(x_k,u_k)， u_k 就是传感器测量值， u_k = [w^{bk},w^{bk+1},a^{bk},a^{bk+1}]
        x_k 本身就带有误差，我们想知道 x_{k+1} 时误差被传播到了什么程度

        f(·)就是根据中值积分写成的线性变换，δ(·) 表示各个时刻的误差
        一阶泰勒展开： x^{hat}_{k+1} + δ(x_{k+1}) = f(x^{hat}_k, u^{hat}_k) + F δ(x_{k}) + G δ(u_{k})
        其中，x^{hat}_{k+1}、x^{hat}_k、u^{hat}_k 为真值
        因此有：                      δ(x_{k+1}) = F δ(x_{k}) + G δ(u_{k})
        δ(u_{k})是传感器参数的误差，也就是那6个标准差，所以有

        | δ(alpha_{bk+1}) |                | δ(alpha_{bk})|         | n^a_k     |
        | δ(theta_{bk+1}) |                | δ(theta_{bk})|         | n^g_k     |     
        | δ(beta_{bk+1})  |      =   F     | δ(beta_{bk}) |  + G    | n^g_{k+1} |
        | δ(b^a_{bk+1})   |                | δ(b^a_{bk})  |         | n^g_{k+1} |
        | δ(b^g_{bk+1})   | 15*1    15*15  | δ(b^g_{bk}   |   15*18 | n_{b^a_k} |
                                                                    | n_{b^g_k} |

        举个例子：
        beta_{bibk+1} = beta_{bibk} + a dt
                      = beta^{hat}_{bibk+1} +  δ(beta_{bk+1}) = beta^{hat}_{bibk} + δ(beta_{bk}) + a dt
        其中 beta^{hat}_{bibk+1} 、beta^{hat}_{bibk} 是真值，而 beta_{bibk+1} 、 beta_{bibk} 是我们通过预积分出来的结果
        即有误差传递式 δ(beta_{bk+1}) = δ(beta_{bk}) + a dt
        因此 F.block<3, 3>(6, 6) = f33 = I

        还有一点：
            δ(x_{k+1}) = F δ(x_{k}) + G δ(u_{k})
            δ(x_{k+2}) = F‘ δ(x_{k+1}) + G’ δ(u_{k})
            δ(x_{k+2}) = F‘’ δ(x_{k}) + G’‘ δ(u_{k})
            所以有，F''= F'*F
            一个时间段内预计分最终得到的雅克比 J = ...*F''*F'*F
        
        ******************************************************************************
        直观说下，求这个F的作用
        f(x) = f(x_0) + f'(x_0)(x-x_0)
        假如说我们知道 f(x_0) 的值, 我们就可以预测一波  f(x)

        我们通过一个函数 f(*)，变量是 b^g，f(b^g) = theta_{bk}
        我们给 b^g 一个小扰动δ{b^g}，预测下此时的 f(*)，有 theta_{bk} + F^{theta}_{b^g} δ{b^g}
        */ 
        if(update_jacobian)
        {
            Vector3d w_x = 0.5 * (_gyr_0 + _gyr_1) - linearized_bg;
            Vector3d a_0_x = _acc_0 - linearized_ba;
            Vector3d a_1_x = _acc_1 - linearized_ba;
            Matrix3d R_w_x, R_a_0_x, R_a_1_x;

            //反对称矩阵
            R_w_x<<0, -w_x(2), w_x(1),
                w_x(2), 0, -w_x(0),
                -w_x(1), w_x(0), 0;
            R_a_0_x<<0, -a_0_x(2), a_0_x(1),
                a_0_x(2), 0, -a_0_x(0),
                -a_0_x(1), a_0_x(0), 0;
            R_a_1_x<<0, -a_1_x(2), a_1_x(1),
                a_1_x(2), 0, -a_1_x(0),
                -a_1_x(1), a_1_x(0), 0;

            MatrixXd F = MatrixXd::Zero(15, 15);
            F.block<3, 3>(0, 0) = Matrix3d::Identity();
            F.block<3, 3>(0, 3) = -0.25 * delta_q.toRotationMatrix() * R_a_0_x * _dt * _dt + 
                                  -0.25 * result_delta_q.toRotationMatrix() * R_a_1_x * (Matrix3d::Identity() - R_w_x * _dt) * _dt * _dt;
            F.block<3, 3>(0, 6) = MatrixXd::Identity(3,3) * _dt;
            F.block<3, 3>(0, 9) = -0.25 * (delta_q.toRotationMatrix() + result_delta_q.toRotationMatrix()) * _dt * _dt;
            F.block<3, 3>(0, 12) = -0.25 * result_delta_q.toRotationMatrix() * R_a_1_x * _dt * _dt * -_dt;
            F.block<3, 3>(3, 3) = Matrix3d::Identity() - R_w_x * _dt;
            F.block<3, 3>(3, 12) = -1.0 * MatrixXd::Identity(3,3) * _dt;
            F.block<3, 3>(6, 3) = -0.5 * delta_q.toRotationMatrix() * R_a_0_x * _dt + 
                                  -0.5 * result_delta_q.toRotationMatrix() * R_a_1_x * (Matrix3d::Identity() - R_w_x * _dt) * _dt;
            F.block<3, 3>(6, 6) = Matrix3d::Identity();
            F.block<3, 3>(6, 9) = -0.5 * (delta_q.toRotationMatrix() + result_delta_q.toRotationMatrix()) * _dt;
            F.block<3, 3>(6, 12) = -0.5 * result_delta_q.toRotationMatrix() * R_a_1_x * _dt * -_dt;
            F.block<3, 3>(9, 9) = Matrix3d::Identity();
            F.block<3, 3>(12, 12) = Matrix3d::Identity();
            //cout<<"A"<<endl<<A<<endl;

            MatrixXd V = MatrixXd::Zero(15,18);
            V.block<3, 3>(0, 0) =  0.25 * delta_q.toRotationMatrix() * _dt * _dt;
            V.block<3, 3>(0, 3) =  0.25 * -result_delta_q.toRotationMatrix() * R_a_1_x  * _dt * _dt * 0.5 * _dt;
            V.block<3, 3>(0, 6) =  0.25 * result_delta_q.toRotationMatrix() * _dt * _dt;
            V.block<3, 3>(0, 9) =  V.block<3, 3>(0, 3);
            V.block<3, 3>(3, 3) =  0.5 * MatrixXd::Identity(3,3) * _dt;
            V.block<3, 3>(3, 9) =  0.5 * MatrixXd::Identity(3,3) * _dt;
            V.block<3, 3>(6, 0) =  0.5 * delta_q.toRotationMatrix() * _dt;
            V.block<3, 3>(6, 3) =  0.5 * -result_delta_q.toRotationMatrix() * R_a_1_x  * _dt * 0.5 * _dt;
            V.block<3, 3>(6, 6) =  0.5 * result_delta_q.toRotationMatrix() * _dt;
            V.block<3, 3>(6, 9) =  V.block<3, 3>(6, 3);
            V.block<3, 3>(9, 12) = MatrixXd::Identity(3,3) * _dt;
            V.block<3, 3>(12, 15) = MatrixXd::Identity(3,3) * _dt;

            //step_jacobian = F;
            //step_V = V;
            /*(cxn)
            δ(x_{k+1}) = F δ(x_{k}) + G δ(u_{k})
            求协方差，E(δ(x_{k+1})) =  F E(δ(x_{k})) F' + G E(δ(u_{k}) G"
            其中 E(δ(u_{k}) 我们是提前标定的，写在config文件中
            在预积分之前，协方差为 E(δ(x_{0})) = 0
            */
            jacobian = F * jacobian;
            covariance = F * covariance * F.transpose() + V * noise * V.transpose();
        }

    }
    

    /**
    * @brief   IMU预积分传播方程
    * @Description  积分计算两个关键帧之间IMU测量的变化量： 
    *               旋转delta_q 速度delta_v 位移delta_p
    *               加速度的biaslinearized_ba 陀螺仪的Bias linearized_bg
    *               同时维护更新预积分的Jacobian和Covariance,计算优化时必要的参数
    * @param[in]   _dt 时间间隔
    * @param[in]   _acc_1 线加速度
    * @param[in]   _gyr_1 角速度
    * @return  void
    */
    void propagate(double _dt, const Eigen::Vector3d &_acc_1, const Eigen::Vector3d &_gyr_1)
    {
        dt = _dt;
        acc_1 = _acc_1;
        gyr_1 = _gyr_1;
        Vector3d result_delta_p;
        Quaterniond result_delta_q;
        Vector3d result_delta_v;
        Vector3d result_linearized_ba;
        Vector3d result_linearized_bg;

        midPointIntegration(_dt, acc_0, gyr_0, _acc_1, _gyr_1, delta_p, delta_q, delta_v,
                            linearized_ba, linearized_bg,
                            result_delta_p, result_delta_q, result_delta_v,
                            result_linearized_ba, result_linearized_bg, 1);

        //checkJacobian(_dt, acc_0, gyr_0, acc_1, gyr_1, delta_p, delta_q, delta_v,
        //                    linearized_ba, linearized_bg);
        delta_p = result_delta_p;
        delta_q = result_delta_q;
        delta_v = result_delta_v;
        linearized_ba = result_linearized_ba;
        linearized_bg = result_linearized_bg;
        delta_q.normalize();
        sum_dt += dt;
        acc_0 = acc_1;
        gyr_0 = gyr_1;  
     
    }

    Eigen::Matrix<double, 15, 1> evaluate(const Eigen::Vector3d &Pi, const Eigen::Quaterniond &Qi, const Eigen::Vector3d &Vi, const Eigen::Vector3d &Bai, const Eigen::Vector3d &Bgi,
                                          const Eigen::Vector3d &Pj, const Eigen::Quaterniond &Qj, const Eigen::Vector3d &Vj, const Eigen::Vector3d &Baj, const Eigen::Vector3d &Bgj)
    {
        Eigen::Matrix<double, 15, 1> residuals;

        Eigen::Matrix3d dp_dba = jacobian.block<3, 3>(O_P, O_BA);
        Eigen::Matrix3d dp_dbg = jacobian.block<3, 3>(O_P, O_BG);

        Eigen::Matrix3d dq_dbg = jacobian.block<3, 3>(O_R, O_BG);

        Eigen::Matrix3d dv_dba = jacobian.block<3, 3>(O_V, O_BA);
        Eigen::Matrix3d dv_dbg = jacobian.block<3, 3>(O_V, O_BG);

        //(cxn)最开始积分的时候，我们设置的bias是linearized_ba，这个值是有误差的，因此预积分的结果deltaQ、delta_v、delta_p也是有误差的，可以描述为 x_{k+1} = f(bias_{k})
        //     经过几轮优化之后，我们得到了更准确的bias，也就是Bai，因此，我们要更新预积分的结果，最准确的方法就是重新积分，但是效率太低
        //     因此，我们使用之前求得的 状态量对bias的导数，线性地更新， x'_{k+1} = f(bias'_{k}) = f(bias_{k}) + J * (\delta{bias_{k}})，注意J我们是求过的
        //     最终得到更准确的预积分值corrected_delta_q、corrected_delta_v、corrected_delta_p，这个就是IMU提供的约束
        Eigen::Vector3d dba = Bai - linearized_ba;
        Eigen::Vector3d dbg = Bgi - linearized_bg;

        Eigen::Quaterniond corrected_delta_q = delta_q * Utility::deltaQ(dq_dbg * dbg);
        Eigen::Vector3d corrected_delta_v = delta_v + dv_dba * dba + dv_dbg * dbg;
        Eigen::Vector3d corrected_delta_p = delta_p + dp_dba * dba + dp_dbg * dbg;

        residuals.block<3, 1>(O_P, 0) = Qi.inverse() * (0.5 * G * sum_dt * sum_dt + Pj - Pi - Vi * sum_dt) - corrected_delta_p;
        residuals.block<3, 1>(O_R, 0) = 2 * (corrected_delta_q.inverse() * (Qi.inverse() * Qj)).vec();
        residuals.block<3, 1>(O_V, 0) = Qi.inverse() * (G * sum_dt + Vj - Vi) - corrected_delta_v;
        residuals.block<3, 1>(O_BA, 0) = Baj - Bai;
        residuals.block<3, 1>(O_BG, 0) = Bgj - Bgi;
        return residuals;
    }

    double dt;
    Eigen::Vector3d acc_0, gyr_0;
    Eigen::Vector3d acc_1, gyr_1;

    const Eigen::Vector3d linearized_acc, linearized_gyr;
    Eigen::Vector3d linearized_ba, linearized_bg;

    Eigen::Matrix<double, 15, 15> jacobian, covariance;
    Eigen::Matrix<double, 15, 15> step_jacobian;
    Eigen::Matrix<double, 15, 18> step_V;
    Eigen::Matrix<double, 18, 18> noise;

    double sum_dt;
    //预积分值
    Eigen::Vector3d delta_p;
    Eigen::Quaterniond delta_q;
    Eigen::Vector3d delta_v;

    std::vector<double> dt_buf;
    std::vector<Eigen::Vector3d> acc_buf;
    std::vector<Eigen::Vector3d> gyr_buf;

};
